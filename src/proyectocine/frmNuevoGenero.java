    public void llenarCodigo() {
        ResultSet rs = gen.obtenerMaxGeneros();
        try {
            while (rs.next()) {
                codGen = rs.getInt("maximo") + 1;
                lblCodigo.setText("" + codGen);
            }
        } catch (Exception e) {
        }
    }
    
