    public void llenarCodigo() {
        Bibliotecas b = new Bibliotecas();
        ResultSet rs = b.obtenerMaxBibliotecas(frm.getNomUsuario());
        try {
            while (rs.next()) {
                cod = rs.getInt("maximo") + 1;
                lblCodigo.setText("" + cod);
            }
        } catch (Exception e) {
        }
    }

